#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	int	c;

	c = ft_strlen("4 3 2 1 1 2 2 2 4 3 2 1 1 2 2 2");
	printf("%d", c);
	return (0);
}
