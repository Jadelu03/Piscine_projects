// 7 % 6 != 0
// 7 % 5 != 0
// 7 % 4 != 0
// 7 % 3 != 0
// 7 % 2 != 0
// 7 % 1 = 0

int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 2)
		return (0);
	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

